test('bridge dials the relay, pipes envelopes by cid, and closes on revoke', async () => {
  const relayConnections = []
  const relayServer = new WebSocketServer({ host: '127.0.0.1', port: 0 })

  relayServer.on('connection', (socket, request) => {
    relayConnections.push({ request, socket })
  })

  await new Promise(resolve => relayServer.once('listening', resolve))

  const relayPort = relayServer.address().port
  const { bridge } = await makeBridge()

  try {
    bridge.setRelayUrl(`http://127.0.0.1:${relayPort}`)

    // The bridge dials out with its host key.
    await new Promise((resolve, reject) => {
      const timer = setTimeout(() => reject(new Error('bridge never dialed the relay')), 3000)
      const poll = setInterval(() => {
        if (relayConnections.length > 0) {
          clearTimeout(timer)
          clearInterval(poll)
          resolve()
        }
      }, 20)
    })

    const { request, socket } = relayConnections[0]

    assert.match(request.url, /^\/host\/[A-Za-z0-9_-]{16,}$/)
    assert.ok(request.headers['x-jarvis-host-key'])

    const state = bridge.getState()

    assert.equal(state.hostId, request.url.split('/').at(-1))

    // Pair a phone "through the relay": DO-style frames over this socket.
    const pairing = bridge.createPairing()
    const payload = parsePairingUrl(pairing.url)
    const key = fromB64url(payload.key)

    const fromBridge = []
    const waiters = []

    socket.on('message', data => {
      const frame = JSON.parse(String(data))
      const waiter = waiters.shift()

      if (waiter) waiter(frame)
      else fromBridge.push(frame)
    })

    const nextFromBridge = () =>
      fromBridge.shift() ??
      new Promise((resolve, reject) => {
        const timer = setTimeout(() => reject(new Error('no frame from bridge')), 3000)

        waiters.push(frame => {
          clearTimeout(timer)
          resolve(frame)
        })
      })

    socket.send(JSON.stringify({ cid: 'c1', t: 'open' }))
    socket.send(JSON.stringify({ cid: 'c1', d: sealEnvelope(key, `pair:${payload.pair}`, 1, 'pair.request', { deviceName: 'Relay iPhone' }), t: 'msg' }))

    const reply = await nextFromBridge()

    assert.equal(reply.t, 'msg')
    assert.equal(reply.cid, 'c1')

    const opened = openEnvelope(key, reply.d)

    assert.equal(opened.type, 'pair.ok')

    // hello over the relay path with the minted identity
    socket.send(
      JSON.stringify({
        cid: 'c1',
        d: sealEnvelope(key, `dev:${opened.body.deviceId}`, 1, 'hello', { deviceToken: opened.body.deviceToken }),
        t: 'msg'
      })
    )

    const helloReply = await nextFromBridge()

    assert.equal(openEnvelope(key, helloReply.d).type, 'hello.ok')

    // Revoking the device force-closes its relay connection.
    bridge.revokeDevice(opened.body.deviceId)

    const frames = [await nextFromBridge(), await nextFromBridge(), await nextFromBridge()]
    const kinds = frames.map(frame => frame.t)

    assert.ok(kinds.includes('close'), `expected a close frame, got ${JSON.stringify(kinds)}`)
  } finally {
    bridge.stop()
    relayServer.close()
  }
})

