// ── Fake upstream dashboard socket ─────────────────────────────────────

const fakeUpstreams = []

function FakeUpstream(url) {
  const upstream = new EventEmitter()

  upstream.url = url
  upstream.readyState = 1
  upstream.sent = []
  upstream.send = text => upstream.sent.push(String(text))
  upstream.close = () => {
    upstream.readyState = 3
    upstream.emit('close')
  }

  fakeUpstreams.push(upstream)
  setImmediate(() => upstream.emit('open'))

  return upstream
}

/** Routes dashboard sockets to fakes; anything else dials out for real. */
function WebSocketRouter(url, options) {
  if (String(url).includes('/api/ws')) {
    return FakeUpstream(url)
  }

  return new WebSocket(url, options)
}

// ── Fake phone speaking the envelope protocol over a real socket ───────

class FakePhone {
  constructor(wsUrl, { key, keyId }) {
    this.key = key
    this.keyId = keyId
    this.seqOut = 0
    this.queue = []
    this.waiters = []
    this.closed = false
    this.ws = new WebSocket(wsUrl)
    this.ws.on('message', data => {
      const frame = openEnvelope(this.key, String(data))
      const waiter = this.waiters.shift()

      if (waiter) waiter(frame)
      else this.queue.push(frame)
    })
    this.ws.on('close', () => {
      this.closed = true

      for (const waiter of this.waiters.splice(0)) {
        waiter(null)
      }
    })
  }

  async open() {
    await new Promise((resolve, reject) => {
      this.ws.once('open', resolve)
      this.ws.once('error', reject)
    })

    return this
  }

  send(type, body, { keyId = this.keyId, rawSeq = null } = {}) {
    this.seqOut = rawSeq ?? this.seqOut + 1
    this.ws.send(sealEnvelope(this.key, keyId, this.seqOut, type, body))
  }

  sendRaw(text) {
    this.ws.send(text)
  }

  async next(timeoutMs = 3000) {
    const queued = this.queue.shift()

    if (queued !== undefined) return queued

    return new Promise((resolve, reject) => {
      const timer = setTimeout(() => reject(new Error('timed out waiting for frame')), timeoutMs)

      this.waiters.push(frame => {
        clearTimeout(timer)
        resolve(frame)
      })
    })
  }

  async waitClosed(timeoutMs = 3000) {
    if (this.closed) return

    await new Promise((resolve, reject) => {
      const timer = setTimeout(() => reject(new Error('socket did not close')), timeoutMs)

      this.ws.once('close', () => {
        clearTimeout(timer)
        resolve()
      })
    })
  }

  close() {
    try {
      this.ws.close()
    } catch {
      void 0
    }
  }
}

async function makeBridge(overrides = {}) {
  const dir = tmpDir()
  const fetchCalls = []
  const bridge = createMobileBridge({
    WebSocketImpl: WebSocketRouter,
    fetchImpl: async (url, options) => {
      fetchCalls.push({ options, url })

      return {
        arrayBuffer: async () => Buffer.from(JSON.stringify({ ok: true, url })),
        headers: { get: () => 'application/json' },
        status: 200
      }
    },
    getDashboard: async () => ({ baseUrl: 'http://127.0.0.1:59999', token: 'dash-token' }),
    hostName: 'Test Mac',
    jarvisHome: dir,
    lanPortPreference: 18776,
    log: () => {},
    ...overrides
  })

  await bridge.start()
  await bridge.setEnabled(true)

  return { bridge, dir, fetchCalls }
}

/** Tests dial loopback directly; payload.lan carries the real LAN addresses. */
function lanUrlFor(bridge) {
  return `ws://127.0.0.1:${bridge.getState().lanPort}/link`
}

async function pairPhone(bridge) {
  const pairing = bridge.createPairing()
  const payload = parsePairingUrl(pairing.url)
  const key = fromB64url(payload.key)
  const lanUrl = lanUrlFor(bridge)
  const phone = await new FakePhone(lanUrl, { key, keyId: `pair:${payload.pair}` }).open()

  phone.send('pair.request', { deviceModel: 'iPhone17,1', deviceName: 'Test iPhone', os: 'iOS 26' })

  const ok = await phone.next()

  assert.equal(ok.type, 'pair.ok')

  return { key, lanUrl, payload, phone, ...ok.body }
}

async function connectDevice(bridge, { deviceId, deviceToken, key, lanUrl }) {
  const phone = await new FakePhone(lanUrl, { key, keyId: `dev:${deviceId}` }).open()

  phone.send('hello', { deviceToken })

  const ok = await phone.next()

  assert.equal(ok.type, 'hello.ok')

  return phone
}

// ── Crypto ──────────────────────────────────────────────────────────────

test('envelope roundtrip preserves type, seq and body', () => {
  const key = generateLinkKey()
  const sealed = sealEnvelope(key, 'dev:abc', 7, 'rpc', { frame: { id: 1, method: 'prompt.submit' } })
  const opened = openEnvelope(key, sealed)

  assert.deepEqual(opened, {
    body: { frame: { id: 1, method: 'prompt.submit' } },
    keyId: 'dev:abc',
    seq: 7,
    type: 'rpc'
  })
})

test('envelopes reject tampering, wrong keys, and key-id swaps', () => {
  const key = generateLinkKey()
  const sealed = sealEnvelope(key, 'dev:abc', 1, 'ping', {})

  const flipped = JSON.parse(sealed)
  const bytes = fromB64url(flipped.c)

  bytes[0] ^= 0xff
  flipped.c = bytes.toString('base64url')
  assert.throws(() => openEnvelope(key, JSON.stringify(flipped)))

  assert.throws(() => openEnvelope(generateLinkKey(), sealed))

  // AAD binds the key id: relabeling dev:abc → dev:evil must fail to open.
  const relabeled = JSON.parse(sealed)

  relabeled.k = 'dev:evil'
  assert.throws(() => openEnvelope(key, JSON.stringify(relabeled)))
})

test('pairing url roundtrips through parse', () => {
  const key = generateLinkKey()
  const url = buildPairingUrl({
    expiresAt: 123456,
    hostId: 'host-id-abcdef123456',
    hostName: 'Test Mac',
    lanUrls: ['ws://192.168.1.20:8776/link'],
    linkKey: key,
    pairingId: 'pairing123',
    relayUrl: 'https://relay.example.workers.dev'
  })
  const parsed = parsePairingUrl(url)

  assert.equal(parsed.host, 'host-id-abcdef123456')
  assert.equal(parsed.pair, 'pairing123')
  assert.equal(parsed.relay, 'https://relay.example.workers.dev')
  assert.deepEqual(fromB64url(parsed.key), key)
  assert.equal(parsePairingUrl('jarvis-link://v2?d=zzz'), null)
})

