test('http proxy honors the allowlist and forwards the dashboard token', async () => {
  const { bridge, fetchCalls } = await makeBridge()

  try {
    const paired = await pairPhone(bridge)

    paired.phone.close()

    const phone = await connectDevice(bridge, { ...paired, lanUrl: paired.lanUrl })

    await phone.next() // upstream.status

    phone.send('http', {
      body: Buffer.from(JSON.stringify({ text: 'hello' })).toString('base64url'),
      contentType: 'application/json',
      id: 'h1',
      method: 'POST',
      path: '/api/audio/speak'
    })

    const ok = await phone.next()

    assert.equal(ok.type, 'http.res')
    assert.equal(ok.body.id, 'h1')
    assert.equal(ok.body.status, 200)
    assert.equal(fetchCalls.length, 1)
    assert.equal(fetchCalls[0].url, 'http://127.0.0.1:59999/api/audio/speak')
    assert.equal(fetchCalls[0].options.headers.authorization, 'Bearer dash-token')

    phone.send('http', { id: 'h2', method: 'GET', path: '/api/env' })

    const refused = await phone.next()

    assert.equal(refused.body.status, 403)
    assert.equal(fetchCalls.length, 1, 'refused paths must not be fetched')

    // Query strings ride along on allowed paths.
    phone.send('http', { id: 'h3', method: 'GET', path: '/api/sessions?limit=20' })

    const sessions = await phone.next()

    assert.equal(sessions.body.status, 200)
    assert.ok(fetchCalls[1].url.endsWith('/api/sessions?limit=20'))

    // Parameterized transcript reads are allowed; other nested paths aren't.
    phone.send('http', { id: 'h4', method: 'GET', path: '/api/sessions/abc123/messages' })

    const transcript = await phone.next()

    assert.equal(transcript.body.status, 200)

    // Session mutations (delete / rename / archive) are proxied…
    phone.send('http', { id: 'h5', method: 'DELETE', path: '/api/sessions/abc123' })

    const deleted = await phone.next()

    assert.equal(deleted.body.status, 200)

    phone.send('http', { id: 'h6', method: 'PATCH', path: '/api/sessions/abc123' })

    const patched = await phone.next()

    assert.equal(patched.body.status, 200)

    // …but mutations outside the session family stay refused.
    phone.send('http', { id: 'h7', method: 'DELETE', path: '/api/files' })

    const denied = await phone.next()

    assert.equal(denied.body.status, 403)

    phone.close()
  } finally {
    bridge.stop()
  }
})

test('large http responses are chunked for relay-safe delivery', async () => {
  const largeBody = Buffer.from(JSON.stringify({
    models: Array.from({ length: 12000 }, (_, index) => `model-${index}`)
  }))
  const { bridge } = await makeBridge({
    fetchImpl: async () => ({
      arrayBuffer: async () => largeBody,
      headers: { get: () => 'application/json' },
      status: 200
    })
  })

  try {
    const paired = await pairPhone(bridge)

    paired.phone.close()

    const phone = await connectDevice(bridge, { ...paired, lanUrl: paired.lanUrl })

    await phone.next() // upstream.status

    phone.send('http', { id: 'big1', method: 'GET', path: '/api/model/options' })

    const chunks = []
    let total = null

    do {
      const frame = await phone.next()

      chunks.push(frame)
      assert.equal(frame.type, 'http.chunk')
      assert.equal(frame.body.id, 'big1')
      assert.equal(frame.body.status, 200)
      assert.equal(frame.body.contentType, 'application/json')
      total = frame.body.total
    } while (chunks.length < total)

    assert.equal(total, chunks.length)
    assert.equal(chunks.map(chunk => chunk.body.index).join(','), Array.from({ length: total }, (_, index) => index).join(','))
    assert.equal(Buffer.from(chunks.map(chunk => chunk.body.body).join(''), 'base64url').toString('utf8'), largeBody.toString('utf8'))

    phone.close()
  } finally {
    bridge.stop()
  }
})

