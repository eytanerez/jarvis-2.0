/**
 * Tests for the mobile bridge (mobile-bridge.cjs + mobile-link-crypto.cjs +
 * mobile-link-store.cjs).
 *
 * Run with: node --test electron/mobile-bridge.test.cjs
 *
 * Why this matters: the bridge is the security boundary between the internet
 * (via the relay) and the local Jarvis brain. Pairing must be one-shot and
 * expiring, device tokens must gate every session, revocation must
 * disconnect immediately, the envelope layer must reject tampering and
 * replays, and the RPC/HTTP allowlists must refuse everything not explicitly
 * meant for phones — before it reaches the dashboard.
 *
 * The "phone" in these tests uses the same crypto module the bridge does;
 * true cross-implementation vectors for the Swift app live in the iOS repo's
 * fixtures (generated by generateTestVectors()).
 */

